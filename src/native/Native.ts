/**
 * Natively implemented concepts.
 * Uppercase elements correspond to constants.
 * Lowercase elements correspond to functions.
 */
export enum Native {
    // Constants (upper case)
    E,
    IMU,
    MASH,
    NIL,
    PI,
    // Functions (lower case)
    abs,
    add,
    div,
    exp,
    inner,
    inv,
    lco,
    mul,
    outer,
    pow,
    rco,
    sin,
    sub,
    succ,
}